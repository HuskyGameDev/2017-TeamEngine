package oasis.audio;

/**
 * 
 * All supported audio formats 
 * 
 * @author Nicholas Hamilton 
 *
 */
public enum AudioFormat {

    /** One channel, 8 bit */ 
    MONO8, 
    
    /** Two channels, 8 bit */ 
    STEREO8, 
    
    /** One channel, 16 bit */ 
    MONO16, 
    
    /** Two channels, 16 bit */ 
    STEREO16
    
}
